# =====================================================================
# 模組名稱：data_cleaning.py
# 功能：清洗 YRBS 2007 原始資料並計算總螢幕使用時間，最後另存新檔
# =====================================================================

import os
import pandas as pd
import numpy as np

# ---------------------------------------------------------------------
# 【慢速小步 1】設定檔案路徑，並確保處理後資料夾存在
# ---------------------------------------------------------------------
# 定義輸入與輸出的路徑（配合你的專案檔案結構）
input_path = "../data/raw/YRBS_2007.csv"
output_dir = "data/processed"
output_path = os.path.join(output_dir, "YRBS_2007_cleaned.csv")

# 如果 data/processed 這個資料夾不存在，Python 會自動幫你建立，避免存檔時報錯
if not os.path.exists(output_dir):
    os.makedirs(output_dir)
    print(f"提示：已自動建立資料夾 -> {output_dir}")

# ---------------------------------------------------------------------
# 【慢速小步 2】讀取原始資料
# ---------------------------------------------------------------------
print("正在從 data/raw/ 讀取原始數據，請稍候...")
df = pd.read_csv(input_path)
print(f"讀取成功！原始資料共有 {df.shape[0]} 筆學生問卷紀錄。\n")

# ---------------------------------------------------------------------
# 【慢速小步 3】欄位確認與防錯機制
# ---------------------------------------------------------------------
# 為了避免每個版本的 CSV 欄位大小寫或拼字不同，我們在這裡先建立變數名稱
# 註：如果執行時報錯說找不到欄位，請對照你的 Codebook 將名稱改成正確的欄位名（例如 Q79, Q80）
x_column = 'PhysicalFighting'
tv_column = 'TelevisionWatching'
game_column = 'ComputerUse'

print("正在檢查核心欄位是否存在...")
required_cols = [x_column, tv_column, game_column]
for col in required_cols:
    if col in df.columns:
        print(f" -> 欄位 [{col}] 檢查存在。")
    else:
        raise KeyError(f"錯誤：在 CSV 中找不到欄位 '{col}'！請檢查說明書中的精確名稱。")

# ---------------------------------------------------------------------
# 【慢速小步 4】清洗缺失值（Dropping NaNs）
# ---------------------------------------------------------------------
print("\n正在剔除這三個欄位中有缺填（NaN）的學生資料...")
# dropna() 會把這三個欄位中，只要有任何一欄是空白的整列資料刪除
df_cleaned = df.dropna(subset=[x_column, tv_column, game_column]).copy()
print(f"剔除完畢！剩餘的有效學生樣本數：{df_cleaned.shape[0]} 筆。")

# ---------------------------------------------------------------------
# 【慢速小步 5】將螢幕時間的「代號」轉換為「實際小時數字」
# ---------------------------------------------------------------------
print("\n正在進行數值轉換（將問卷代號還原為實際小時數）...")

# 建立對照字典 (Mapping)
# 代號 1 轉成 0小時, 2 轉成 0.5小時(不到1小時), 3 轉成 1小時... 7 轉成 5小時
screen_time_map = {
    1: 0.0,       # 不看 / 不玩
    2: 0.5,       # 不到 1 小時
    3: 1.0,       # 1 小時
    4: 2.0,       # 2 小時
    5: 3.0,       # 3 小時
    6: 4.0,       # 4 小時
    7: 5.0        # 5 小時以上
}

# 使用 .map() 函數把原本的代號換成數字
df_cleaned['TV_Hours'] = df_cleaned[tv_column].map(screen_time_map)
df_cleaned['Game_Hours'] = df_cleaned[game_column].map(screen_time_map)

# ---------------------------------------------------------------------
# 【慢速小步 6】計算 Y 值：每日總螢幕使用時間 (Total Screen Time)
# ---------------------------------------------------------------------
# 將電視小時數與電玩小時數相加
df_cleaned['Total_Screen_Time'] = df_cleaned['TV_Hours'] + df_cleaned['Game_Hours']
print(" -> 成功建立新欄位 [Total_Screen_Time] (Y值)！")

# ---------------------------------------------------------------------
# ✨【本次新增的慢速小步】✨：只篩選出你想要的 4 個欄位
# ---------------------------------------------------------------------
print("\n正在過濾欄位，只保留你指定的四個項目...")

# 這裡我們排好順序：打架次數、原始電視、原始電玩、相加後的總時間
final_columns = [x_column, tv_column, game_column, 'Total_Screen_Time']

# 讓 df_cleaned 只留下這四個欄位
df_cleaned = df_cleaned[final_columns]

# ---------------------------------------------------------------------
# 【慢速小步 7】另存新檔到 processed 資料夾
# ---------------------------------------------------------------------
print(f"正在將【精簡 4 欄版】數據儲存至：{output_path} ...")
df_cleaned.to_csv(output_path, index=False)
print("恭喜！四欄精簡版資料清洗已全部完成。✨")